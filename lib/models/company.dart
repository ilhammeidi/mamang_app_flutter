class Company {
  final String id;
  final String name;
  final String logo;
  final String title;
  final String desc;
  final String version;
  final String prefix;
  final String footerText;
  final double year;
  final String logoText;
  final String url;

  Company({
    required this.id,
    required this.name,
    required this.logo,
    required this.title,
    required this.desc,
    required this.version,
    required this.prefix,
    required this.footerText,
    required this.year,
    required this.logoText,
    required this.url
  });
}